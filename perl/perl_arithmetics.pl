#!/usr/bin/perl
# Simple Perl Arithmetics (perl_arithmetics.pl)
# 2013-09-26

# Perl Addition
$addition=5+5.3;
print "Perl Addition:\n5 + 5 = ".$addition."\n";

# Perl Subtraction
$subtraction=100-23;
print "Perl Subtraction:\n100 - 23 = ".$subtraction."\n";

# Perl Multiplication
$multiplication=3*9;
print "Perl Multiplication:\n3 x 9 = ".$multiplication."\n";

# Perl Division
$division=45/5;
print "Perl Division:\n45 : 5 = ".$division."\n";

# Perl Modulus
$modulus=10%3;
print "Perl Modulus:\n10 % 3 = ".$modulus."\n";
 
# Perl Exponential 
$exponential=3**4;
print "Perl Exponential:\n3 ** 4 = ".$exponential."\n"; 
